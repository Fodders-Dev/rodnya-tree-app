/// P0 (мамин баг): карточка родственника должна открываться из любого
/// контекста, а не только из выбранного дерева. Сервисы, умеющие отвечать
/// «в каком дереве живёт этот человек», публикуют это через capability —
/// тот же паттерн, что и остальные *_capable_* интерфейсы (graph,
/// identity-conflicts, …). Экран карточки использует его в цепочке
/// резолва: явный treeId из роута → кэш → выбранное дерево → полный обход.
abstract class PersonTreeResolutionCapableFamilyTreeService {
  /// Синхронный ответ из кэша person→tree (без сети). Кэш наполняется
  /// каждым успешным fetch'ем людей (getRelatives / graph snapshot /
  /// getPersonById / мутации), так что для недавно виденных людей ответ
  /// мгновенный. `null` — человек кэшу не известен.
  String? cachedTreeIdForPerson(String personId);

  /// Полный резолв: кэш → локальное хранилище → перебор деревьев
  /// пользователя (GET людей в каждом). Найденное дерево кэшируется.
  /// `null` — человек не найден ни в одном доступном дереве.
  Future<String?> resolveTreeIdForPerson(String personId);
}
